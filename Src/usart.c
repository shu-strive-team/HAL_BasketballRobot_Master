{
    while(__HAL_UART_GET_FLAG(&huart4,UART_FLAG_TC)==RESET){}; 
    UART4->DR=c;
} 

//函数说明：将单精度浮点数据转成4字节数据并存入指定地址 
//target:目标单精度数据
//buf:待写入数组
//beg:指定从数组第几个元素开始写入
//函数无返回 
void Float2Byte(float *target,unsigned char *buf,unsigned char beg)
{
    unsigned char *point;
    point = (unsigned char*)target;	  //得到float的地址
    buf[beg]   = point[0];
    buf[beg+1] = point[1];
    buf[beg+2] = point[2];
    buf[beg+3] = point[3];
}
// *data:	所发送数据数组，最大长度为6
// n:		发送数据所占字节数
void UART_SendDataToPC(float *data, u8 n)
{
	int8_t i = 0;
	unsigned char *point;
	point = (unsigned char*)data;	  //得到float的地址
	if(n > 24)
		n = 24;
	usart_send_char('$');
	usart_send_char('%');
	usart_send_char(n * 0.25);
//	
	for(i = 0; i < n;i ++)
		usart_send_char(point[i]);
}

